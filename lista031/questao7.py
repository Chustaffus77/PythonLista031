#7) Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.

print("Você poderia inserir 3 números pra tirarmos a média aritmética deles?")

n1 = float(input("Insira o Primeiro Número:"))
n2 = float(input("O Segundo Número:"))
n3 = float(input("Por fim o Terceiro Número:"))

med = (n1 + n2 + n3)/3

print("Segundo meus cálculos, a média aritmética desses 3 números equivale a",med,". Espero ter Ajudado!")



