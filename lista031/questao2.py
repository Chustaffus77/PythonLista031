print("Olá! Por favor insira 4 números inteiros para um calculo rápido!")

n1 = int(input("Insira o primeiro número:"))
n2 = int(input("Agora, o segundo número:"))
n3 = int(input("Terceiro número:"))
n4 = int(input("E por fim, o quarto número:"))

soma = n1 + n2 + n3 + n4
produto = n1 * n2 * n3 * n4

print("O resultado da soma dos números registrados é igual a",soma,".Já produto tem como valor:",produto)

